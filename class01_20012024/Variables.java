package class01_20012024;

public class Variables {

	public static void main(String[] args) {
		//byte
		byte byteVar = 127;
		System.out.println("the byte value is::::::"+ byteVar);
		//short
		short shortVar = 32767;
		System.out.println("the short value is:::::"+shortVar);
		//int
		int intVar = 66555655;
		System.out.println("the int value is::::::"+intVar);
		//long
		long longVar = 1234567899;
		System.out.println("the long value is:::::"+longVar);
		//float
		float floatVar = 23.67f;
		System.out.println("the value for float is::::::"+floatVar);
		//double
		double doubleVar = 4.66666;
		System.out.println("the value for double is::::"+doubleVar);
		//char
		char charVar = 'd';
		System.out.println("the value of char is::::"+charVar);
		//boolean
		boolean boolVar = true;
		System.out.println("the value of boolean is::::"+boolVar);

	}

}
