package class01_20012024;

public class Conditional_If_ElseIf {

	public static void main(String[] args) {
		int a = 5;
		if(a < 3) {
			System.out.println("value is less than 3");
		}else if((a>3) && (a<5)) {
			System.out.println("value is greater than 3 and less than 5");
		}
		else if(a >= 5) {
			System.out.println("value is greater than or equal to than 5");
		}
		
		System.out.println("Program Terminated!!!");
		

	}

}
